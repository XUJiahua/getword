def build_bank(raw: dict, unit_map: list[dict],
               char_word_idx: dict[str, list[str]]) -> dict:
    """
    把按课次（key=(kind, no)）的原始数据，按 unit_map 聚合成按单元的 bank。
    unit_map 的 lessons 元素支持两种形式：
      (no, title)            ← 三-六年级（kind 默认为空）
      (kind, no, title)      ← 一/二年级（拼音/识字/课文 三类节）
    优先用本课词语表；若该课无词语表（古诗词、略读、一年级整本），则把
    识字表/写字表的字通过逆索引换成双字词，避免单字听写歧义。
    """
    by_key = {(l.get("kind", ""), l["lesson_no"]): l for l in raw["lessons"]}

    units = []
    for unit in unit_map:
        words: list[str] = []
        seen: set[str] = set()
        lesson_summary = []
        for entry in unit["lessons"]:
            if len(entry) == 2:
                kind, no, title = "", entry[0], entry[1]
            else:
                kind, no, title = entry
            data = by_key.get((kind, no), {"words": [], "write_chars": [],
                                           "recognize_chars": []})

            if data["words"]:
                source_words = data["words"]
            else:
                # 把单字按 char→word 逆索引换成代表词；找不到的留单字（少数）。
                # 写字表优先（必须会写），识字表补充（认读）。
                seen_chars: set[str] = set()
                source_words = []
                for ch in data["write_chars"] + data["recognize_chars"]:
                    if ch in seen_chars:
                        continue
                    seen_chars.add(ch)
                    source_words.append(char_to_practice_word(ch, char_word_idx))

            for w in source_words:
                if w not in seen:
                    seen.add(w)
                    words.append(w)
            lesson_summary.append({
                "kind": kind or None,
                "no": no,
                "title": title,
                "wordCount": len(data["words"]),
                "writeCharCount": len(data["write_chars"]),
                "recognizeCharCount": len(data["recognize_chars"]),
            })

        units.append({
            "key": unit["key"],
            "name": unit["name"],
            "words": words,
            "lessons": lesson_summary,
        })

    return {
        "name": f"{raw['grade']}年级{raw['semester']}册".replace("4", "四").replace("1", "一")
                .replace("2", "二").replace("3", "三").replace("5", "五").replace("6", "六"),
        "short": f"{raw['grade']}{raw['semester']}".replace("4", "四").replace("1", "一")
                 .replace("2", "二").replace("3", "三").replace("5", "五").replace("6", "六"),
        "grade": raw["grade"],
        "semester": raw["semester"],
        "source": {
            "type": "pep-textbook",
            "edition": raw["source"].get("publisher", ""),
            "extractedAt": raw["source"].get("extracted_at"),
            "pdfFilename": raw["source"].get("pdf_filename"),
            "label": "部编版 · 词语表",
        },
        "units": units,
    }


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--in", dest="src", required=True, type=Path)
    ap.add_argument("--out", required=True, type=Path)
    ap.add_argument("--bank-key", required=True, help="如 grade4b")
    args = ap.parse_args()

    raw = json.loads(args.src.read_text(encoding="utf-8"))
    if args.bank_key not in UNIT_MAPS:
        raise SystemExit(f"unknown bank key {args.bank_key}; "
                         f"add unit map in UNIT_MAPS first")

    char_word_idx = build_char_to_word_index(args.src.parent)
    bank = build_bank(raw, UNIT_MAPS[args.bank_key], char_word_idx)

    js_payload = json.dumps(bank, ensure_ascii=False, indent=2)
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(
        f"// auto-generated by scripts/build_bank.py — do not edit by hand\n"
        f"// source: {args.src.name}\n"
        f"window.GRADE_DATA = window.GRADE_DATA || {{}};\n"
        f"window.GRADE_DATA.{args.bank_key} = {js_payload};\n",
        encoding="utf-8",
    )

    total_words = sum(len(u["words"]) for u in bank["units"])
    print(f"wrote {args.out}")
    print(f"  {len(bank['units'])} units, {total_words} words total")
    for u in bank["units"]:
        print(f"  {u['key']}: {len(u['words']):>3} 词  {u['name']}")


if __name__ == "__main__":
    main()
