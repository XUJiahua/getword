def build_char_to_word_index(sources_dir: Path) -> dict[str, list[str]]:
    """
    扫描所有册的词语表，建立「字 → 含该字的词列表」逆索引。
    用于把识字表里的孤字（无词语上下文）换成可听写的双字词，消除拼音歧义。
    返回: {char: [shortest 2-char word, ..., longer words]}（已去重）
    """
    idx: dict[str, set[str]] = {}
    for f in sorted(sources_dir.glob("grade*.json")):
        data = json.loads(f.read_text(encoding="utf-8"))
        for lesson in data.get("lessons", []):
            for word in lesson.get("words", []):
                if 2 <= len(word) <= 4:
                    for ch in word:
                        idx.setdefault(ch, set()).add(word)
    # 偏好 2 字词；同长度按字典序稳定。
    return {ch: sorted(ws, key=lambda w: (len(w), w)) for ch, ws in idx.items()}


def char_to_practice_word(ch: str, idx: dict[str, list[str]]) -> str:
    """字 → 用于听写的代表词。优先教材内 2 字词，其次手工兜底，最后返回单字。"""
    candidates = idx.get(ch)
    if candidates:
        return candidates[0]
    if ch in FALLBACK_CHAR_WORDS:
        return FALLBACK_CHAR_WORDS[ch]
    return ch

